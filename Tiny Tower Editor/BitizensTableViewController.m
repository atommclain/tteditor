- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return self.towerController.tower.bitizens.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"BitizenCell";
    BitizenCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    Bitizen *bitizen = self.towerController.tower.bitizens[indexPath.row];
    if (bitizen.workFloor == -1) {
        cell.workLabel.text = NSLocalizedString(@"Unemployed", @"String for indicating a bitizne is jobless");
    } else {
        cell.workLabel.text = [NSString stringWithFormat:@"%i", bitizen.workFloor];
    }
    
    cell.homeLabel.text = [NSString stringWithFormat:@"%i", bitizen.homeFloor];
    
    return cell;
}

